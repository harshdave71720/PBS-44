class MockAvailabilityRepository implements AvailabilityRepository {
  async getMonthAvailability({
    bhavanType,
    year,
    month,
  }: MonthAvailabilityQuery): Promise<AvailabilityRecord[]> {
    const monthPrefix = `${year}-${String(month).padStart(2, "0")}-`
    return MOCK_AVAILABILITY_RECORDS.filter(
      (record) => record.bhavanType === bhavanType && record.date.startsWith(monthPrefix)
    )
  }
}

